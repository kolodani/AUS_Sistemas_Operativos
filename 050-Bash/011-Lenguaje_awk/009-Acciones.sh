# ACCIONES

# Una accion = una o mas sentencias awk y se encierra entre llaves {}
# Las sentencias son separadas por caracteres nueva-linea o ; (punto y coma)

# Tipos de sentencias
# - Expresiones
#    Invocacion de funciones, asignacion de variables, variables
#    y funciones built-in
# - Sentencias de control
#    Similares a las del lenguaje C: if, for, while, do-while, break, continue
# - Sentencias compuestas
#    Consisten en una o mas sentencias encerradas entre llaves, bloques
# - Control de entrada
#    getline y next
# - Sentencias de salida
#    print y printf
# - Sentencia de borrado
#    para eliminar elementos de una array